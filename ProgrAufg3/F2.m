function [ F,DF ] = F2( x )
% n=4
    F=zeros(4,1);
    F(1)= 2*(x(1)-1)+3*(x(2)+x(1)-2)*abs(x(2)+x(1)-2)+4000*(x(1)*x(1)-x(3))*x(1);
    F(2)= 3*(x(2)+x(1)-2)*abs(x(2)+x(1)-2) + 8*x(2)*(x(2)*x(2)+1-x(3)-x(4))^3;
    F(3)= 2000*(x(3)-x(1)*x(1))+4*(x(4)-x(2)*x(2)+x(3)-1)^3;
    F(4)= 4*(x(4)-x(2)*x(2)+x(3)-1)^3;
    DF(1,1)= 2+ 6*abs(x(2)+x(1)-2) + 4000*(x(1)*x(1)-x(3)) +8000*x(1)*x(1);
    DF(1,2)= 6*abs(x(2)+x(1)-2);
    DF(1,3)= -4000*x(1);
    DF(1,4)=0;
    DF(2,2)= 6*abs(x(2)+x(1)-2) + 8*(x(2)*x(2)+1-x(3)-x(4))^3 + 48*x(2)*x(2)*(x(2)*x(2)+1-x(3)-x(4))^2;
    DF(2,3)= -24*x(2)*(x(2)*x(2)+1-x(3)-x(4))^2;
    DF(2,4)= DF(2,3);
    DF(3,3)= 2000 + 12*(x(4)-x(2)*x(2)+x(3)-1)^2;
    DF(3,4)= 12*(x(4)-x(2)*x(2)+x(3)-1)^2;
    DF(4,4) = 12*(x(4)-x(2)*x(2)+x(3)-1)^2;
    for i=2:4
        for j= 1:i-1
            DF(i,j)=DF(j,i);
        end
    end

end